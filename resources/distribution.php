<p>In order to sharedrop on a chain, a snapshot must be taken of all the shareholders at a certain date and time. This snapshot is then incorporated into the genesis block, dividing a percentage of shares at the birth of the new chain amongst shareholders of the snapshotted chain. Snapshots are announced at least 2 weeks in advance, to allow holders of the target chain to collect their shares from the order books of a central exchange, or any wallet where the private keys are not controlled by the shareholder.</p>

<p>Sharedropping has proved to be a powerful tool for building momentum for new blockchain based projects. Since mid 2014, there are four independent projects currently underway that have recognized the significance of the BitShares social consensus, and used it to gain the support of BitShares holders. These are Peertracks, Play, Sparkle and DPOS Protoshares. Anyone who now buys or holds BitShares could be the potential recipient of countless more sharedrops in the future, as word spreads of the practical advantages of the BitShares toolkit.</p>
     
     
