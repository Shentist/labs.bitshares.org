<p>BTS shareholders are a diverse group of people united with the common purpose of supporting and promoting the <a href="../resources/vision.php">BitShares vision.</a>  Since its formation, BitShares has established a firm set of guidelines to be followed in order to keep the community aligned both philosophically and economically. These guidelines are referred to as the “social consensus”.</p>

<p>The social consensus is an agreement that new projects and initiatives wanting to use the BitShares open source software must give at least 10% of their initial stake to the holders of BTS. We believe that blockchain technology is not only an amazing economic experiment, but an important social one as well. The idea behind sharedropping is that any future chain will always benefit by choosing to align itself with the ones who worked hard at making the technology possible. This is not a mandatory requirement, but more of a “thank you” to the supporters and developers that have donated time and resources to bringing it into the world.</p>

<p>BitShares has set an example of this social agreement by establishing its own sharedropping standards. The seed allocation of BitShares took place over a 1 year period, from November 2013 to November 2014, and was sharedropped on the following chains:</p>



